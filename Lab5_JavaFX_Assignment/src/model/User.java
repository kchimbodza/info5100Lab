package model;

/**
 *
 * @author Admin
 */
public class User {
    private String name, email;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
       return """ 
           Name: """ + " " + name + "\n" +
           "Age: " + age + "\n" +
           "Email: " + email + "\n";  
    }
}
